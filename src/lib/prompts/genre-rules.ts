export function getGenreRules(genre: string): string {
  const rules: Record<string, string> = {
    '玄幻': `## 玄幻题材写作规范
- 力量体系描写要克制：战斗场面以招式意图和结果为主，避免长篇数值说明
- 世界观揭示遵循"行动中带出"原则，禁止旁白式灌输
- 修炼/升级过程必须伴随情感冲突或利益博弈，避免流水账
- 对话占比建议：30-40%`,

    '都市': `## 都市题材写作规范
- 职业细节要真实可信：对话符合身份，避免"所有人都在演讲"
- 场景切换快：用一个细节锚定场景，不要大段环境描写
- 对话占比建议：40-50%
- 人际冲突优先于物理冲突：关系变化是最核心的张力来源`,

    '科幻': `## 科幻题材写作规范
- 技术描写用"感知法"：通过角色的使用体验揭示技术，而非旁白说明
- 对话占比建议：30%
- 科幻场景注重"陌生感"：环境要让人觉得不对劲`,

    '言情': `## 言情题材写作规范
- 情感用微动作和潜台词传达：眼神、距离变化、语气转折
- 对话占比建议：45-55%
- 禁止直白表白除非是高潮节点，日常互动用含蓄方式表达
- 环境描写服务于情绪`,

    '武侠': `## 武侠题材写作规范
- 武打场面克制：一场战斗控制在500字以内，重点在意图和心理博弈
- 江门规矩和人情世故比武力更重要
- 对话占比建议：35-45%`,

    '悬疑': `## 悬疑题材写作规范
- 线索投放要有层次：每个场景最多暗示1-2个线索，禁止一次性灌输
- 信息差是核心张力：角色知道的和读者知道的要有错位
- 对话占比建议：35-45%，用对话制造误导和暗示`,

    '历史': `## 历史题材写作规范
- 虽是架空世界，但制度、礼仪、称谓要自洽
- 大事件用小人物视角切入，避免编年体流水账
- 对话占比建议：30-40%`,

    '奇幻': `## 奇幻题材写作规范
- 世界观设定要精简：核心设定不超过3个，避免设定堆砌
- 魔法/超自然元素要有代价和限制
- 对话占比建议：30-40%`,
  }

  return rules[genre] ?? ''
}
